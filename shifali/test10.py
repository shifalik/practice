#Arithmetic Operators

a = 2
b = 3

print("Value of a:", a, "\nValue of b:", b, "\n")

c = a + b 
print("Addition:", c)

d = a - b
print("Subtraction:", d)

e = a * b
print("Multiplication:", e)

f = b / a
print("Division:", f)

g = b % a
print("Modulus:", g)

h = a ** b
print("Exponent:", h)

i = a // b
print("Floor Division:", i)

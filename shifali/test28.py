#iterating by sequence index
#this is an alternative way of iterating
#through each item by index offset into 
#the sequence itself

fruits = ['banana', 'apple', 'mango']

for index in range(len(fruits)):
    print("Current Fruit:", fruits[index])
    
print("End of program")

# len() is a built in function, which
#provides the total number of elements in 
#the tuple as well as the range() built-in
#function to give us the actual sequence to 
